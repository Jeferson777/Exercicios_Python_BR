'''
1-Faça um Programa que peça dois números e imprima o maior deles.
'''
num_1 = input('Insira um número: ')
num_2 = input('Insira outro número: ')

if num_1 > num_2:
    print(f'Variável num_1: {num_1} é maior que a variável num_2: {num_2} ')
else:
    print(f'Variável num_2: {num_2} é maior que a variável num_1: {num_1} ')